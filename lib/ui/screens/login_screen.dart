Future<void> login(String email, String pass, BuildContext context ) async {
 
  // make a api call to authenticate user and get token
  // if token is not null then navigate to home screen
  // else show error message

   final Uri url = Uri.parse('http://localhost:3000/api/users/login'); // Convert the URL to a Uri object

 
final Map<String, dynamic> data = {
  'user': {
    'email': email,
    'pass': pass,
  },
};


final response = await http.post(
  url,
  headers: <String, String>{
    'Content-Type': 'application/json; charset=UTF-8',
  },
  body: jsonEncode(data),
);

if (response.statusCode == 200) {
    // Successful authentication, parse the token from the response
 
    final Map<String, dynamic> data = json.decode(response.body);

    print(data);
 
    // Check if the token is not null
    if (data['token'] != null) {
      // Token is not null, navigate to the home screen
    // ignore: use_build_context_synchronously
      Navigator.pushReplacementNamed(context, '/home'); // Replace current screen with the home screen
    } else {
      // Token is null, show an error message
    // ignore: use_build_context_synchronously
      showDialog(
        context: context,
        builder: (BuildContext context) {
          return AlertDialog(
            title: const Text('Authentication Failed'),
            content: const Text('Invalid email or password. Please try again.'),
            actions: <Widget>[
              TextButton(
                onPressed: () {
                  Navigator.of(context).pop();
                },
                child: const Text('OK'),
              ),
            ],
          );
        },
      );
    }
  } else {
    // Error occurred in the API call
    // ignore: use_build_context_synchronously
    showDialog(
      context: context,
      builder: (BuildContext context) {
        return AlertDialog(
          title: const Text('Error'),
          content: const Text('An error occurred. Please try again later.'),
          actions: <Widget>[
            TextButton(
              onPressed: () {
                Navigator.of(context).pop();
              },
              child: const Text('OK'),
            ),
          ],
        );
      },
    );
  }


}
